SET SERVEROUTPUT ON

DECLARE
  CURSOR CUR IS
    SELECT
      TABLE_NAME
    FROM
      USER_TABLES
    WHERE
      TABLE_NAME LIKE 'SRC%'
    ;

  V_STMT VARCHAR2(1000);

BEGIN
  FOR REC IN CUR LOOP
    DBMS_OUTPUT.PUT_LINE(V_STMT);
    V_STMT := 'ALTER TABLE ' || REC.TABLE_NAME || ' LOGGING';
    EXECUTE IMMEDIATE V_STMT;
  END LOOP;
END;
/

quit

